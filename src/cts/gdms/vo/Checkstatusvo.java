package cts.gdms.vo;

public class Checkstatusvo {
	private String bookingid;
	private String expecteddate;
	private String giftsubid;
	private String deliverystatus;

	public String getBookingid() {
		return bookingid;
	}

	public void setBookingid(final String bookingid) {
		this.bookingid = bookingid;
	}

	public String getExpecteddate() {
		return expecteddate;
	}

	public void setExpecteddate(final String expecteddate) {
		this.expecteddate = expecteddate;
	}

	public String getGiftsubid() {
		return giftsubid;
	}

	public void setGiftsubid(final String giftsubid) {
		this.giftsubid = giftsubid;
	}

	public String getDeliverystatus() {
		return deliverystatus;
	}

	public void setDeliverystatus(final String deliverystatus) {
		this.deliverystatus = deliverystatus;
	}

}
