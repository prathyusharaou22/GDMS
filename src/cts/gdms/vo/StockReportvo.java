package cts.gdms.vo;

public class StockReportvo {

	public String giftsubid;
	public String giftdescription;
	public int quantity;
	public int cost;
	
	
	public String getGiftsubid() {
		return giftsubid;
	}
	final public void setGiftsubid(final String giftsubid) {
		this.giftsubid = giftsubid;
	}
	 public String getGiftdescription() {
		return giftdescription;
	}
	final public void setGiftdescription(final String giftdescription) {
		this.giftdescription = giftdescription;
	}
	public int getQuantity() {
		return quantity;
	}
	final public void setQuantity(final int quantity) {
		this.quantity = quantity;
	}
	public int getCost() {
		return cost;
	}
	final public void setCost(final int cost) {
		this.cost = cost;
	}
	
}
